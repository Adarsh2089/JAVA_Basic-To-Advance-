/*   Methods of HashMap ----
   1) .put(key,value) - adds or updates mapping.
   2) .get(key) - returns value for key.
   3) .getOrDefault(key,default) - returns value or default.
   4) .containsKey(key) - checks key existence.
   5) .containsValue(value) - checks value existence.
   6) .remove(key) - removes key-value pair.
   7) .keySet() - returns all keys.
   8) .values() - returns all values.
   9) .entrySet() - returns key-value pairs.
  10) .isEmpty() - checks if map is empty.
  11) .size() - returns number of mappings.
  12) .clear() - removes all mappings.
*/




