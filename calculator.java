public class calculator {
 
    // This is a Class Only For Calculating Things.
    
    public int add(int n1,int n2){
        int r = n1+n2;
        return r;
    }

    public int Mul(int n1,int n2){
        int r = n1*n2;
        return r;
    }

    public int Div(int n1,int n2){
        int r = n1/n2;
        return r;
    }

    public int Mod(int n1,int n2){
        int r = n1%n2;
        return r;
    }

}
