package DAY02;

public class loops {
    public static void main(String[] args){
        int i=0;

    // while loop
        while (i<=10)
        {
          System.out.println(i);
          i++;
        }

    // do while loop
        do{
            System.out.println(i);    // it will print to 0 to 11 cause , it checks the condition after printing. 
        }while(i<=10);

    // for loop
    for (int j = 0; j <= 10; j++)
    {
       System.out.println(j);
    }    


    /*  Which loop to use in JAVA...
         it will be depends upon the uses .
    */
    
    }
}
